import UIKit

extension UIFont {
    class var navBarTitle: UIFont {
        return UIFont.systemFont(ofSize: 21.0, weight: .bold)
    }

    class var navBarLargeTitle: UIFont {
        return UIFont.systemFont(ofSize: 35.0, weight: .bold)
    }
}
